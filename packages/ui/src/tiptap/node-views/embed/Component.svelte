<script lang="ts">
  import { css, cx } from '@typie/styled-system/css';
  import { center, flex } from '@typie/styled-system/patterns';
  import { onMount, tick } from 'svelte';
  import EllipsisIcon from '~icons/lucide/ellipsis';
  import FileUpIcon from '~icons/lucide/file-up';
  import Trash2Icon from '~icons/lucide/trash-2';
  import { createFloatingActions } from '../../../actions';
  import { Button, Icon, Menu, MenuItem, RingSpinner, TextInput } from '../../../components';
  import { NodeView } from '../../lib';
  import { calculateResizeProportion, checkAndAdjustProportion, getMaxContentHeight } from '../utils/resize';
  import type { NodeViewProps } from '../../lib';
  import type { ResizeData } from '../utils/resize';

  type Props = NodeViewProps;

  let { node, editor, selected, updateAttributes, deleteNode, HTMLAttributes }: Props = $props();

  let attrs = $state(node.attrs);
  $effect(() => {
    attrs = node.attrs;
  });

  let inflightUrl = $state<string>();
  let inflight = $state(false);
  let pickerOpened = $state(false);
  let inputEl = $state<HTMLInputElement>();
  let embedContainerEl = $state<HTMLDivElement>();
  let containerEl = $state<HTMLDivElement>();
  let proportion = $state(node.attrs.proportion || 1);

  const pageLayout = $derived(editor?.current.storage.page?.layout);
  const maxContentHeight = $derived(getMaxContentHeight(pageLayout));

  $effect(() => {
    if (node.attrs.proportion !== undefined) {
      proportion = node.attrs.proportion;
    }
  });

  $effect(() => {
    if (pageLayout && attrs.html && (attrs.id || inflight)) {
      proportion = checkAndAdjustProportion(proportion, containerEl, pageLayout, updateAttributes);
    }
  });

  $effect(() => {
    pickerOpened = selected;
  });

  $effect(() => {
    if (pickerOpened) {
      tick().then(() => {
        inputEl?.focus();
      });
    }
  });

  const { anchor, floating } = createFloatingActions({
    placement: 'bottom',
    offset: 4,
    onClickOutside: () => {
      pickerOpened = false;
    },
  });

  const handleInsert = async () => {
    if (!inflightUrl) {
      return;
    }

    inflight = true;
    try {
      // eslint-disable-next-line @typescript-eslint/no-non-null-assertion
      const attrs = await editor!.current.storage.unfurlEmbed(/^[^:]+:\/\//.test(inflightUrl) ? inflightUrl : `https://${inflightUrl}`);
      updateAttributes(attrs);
    } finally {
      inflight = false;
    }
  };

  export const handle = (event: CustomEvent) => {
    if (event.type === 'inflight') {
      inflight = true;
      inflightUrl = event.detail.url;
    } else if (event.type === 'success') {
      inflight = false;
      inflightUrl = undefined;
      updateAttributes(event.detail.attrs);
    } else if (event.type === 'error') {
      inflight = false;
      inflightUrl = undefined;
    }
  };

  let initialResizeData: ResizeData | null = null;

  const handleResizeStart = (event: PointerEvent, reverse: boolean) => {
    if (!containerEl) {
      return;
    }

    const target = event.currentTarget as HTMLElement;
    target.setPointerCapture(event.pointerId);

    initialResizeData = {
      x: event.clientX,
      width: containerEl.clientWidth,
      proportion,
      reverse,
    };
  };

  const handleResize = (event: PointerEvent) => {
    const target = event.currentTarget as HTMLElement;
    if (!target.hasPointerCapture(event.pointerId) || !initialResizeData || !containerEl) {
      return;
    }

    const dx = (event.clientX - initialResizeData.x) * (initialResizeData.reverse ? -1 : 1);
    proportion = calculateResizeProportion(dx, initialResizeData, containerEl, pageLayout);
  };

  const handleResizeEnd = (event: PointerEvent) => {
    const target = event.currentTarget as HTMLElement;
    target.releasePointerCapture(event.pointerId);
    updateAttributes({ proportion });
  };

  onMount(() => {
    if (!document.querySelector('script#iframely-embed')) {
      const script = document.createElement('script');
      script.id = 'iframely-embed';
      script.async = true;
      script.src = 'https://cdn.iframe.ly/embed.js';
      document.head.append(script);
    }

    if (attrs.html && attrs.title) {
      const iframe = embedContainerEl?.querySelector('iframe');
      if (iframe) {
        iframe.setAttribute('title', attrs.title);
      }
    }
  });
</script>

<NodeView style={css.raw({ display: 'flex', justifyContent: 'center', width: 'full' })} {...HTMLAttributes}>
  <div
    bind:this={containerEl}
    style:width={`${proportion * 100}%`}
    class={cx('group', css({ position: 'relative' }))}
    data-drag-handle
    draggable
  >
    {#if attrs.id}
      {#if attrs.html}
        <div
          bind:this={embedContainerEl}
          style:max-height={maxContentHeight ? `${maxContentHeight}px` : undefined}
          class={css({ display: 'contents' }, editor?.current.isEditable && { pointerEvents: 'none' })}
        >
          <!-- eslint-disable-next-line svelte/no-at-html-tags -->
          {@html attrs.html}
        </div>
      {:else}
        <svelte:element
          this={editor?.current.isEditable ? 'div' : 'a'}
          class={flex({ borderWidth: '1px', borderColor: 'border.subtle', borderRadius: '6px' })}
          {...!editor?.current.isEditable && { href: attrs.url, target: '_blank', rel: 'noopener noreferrer' }}
        >
          <div class={flex({ direction: 'column', grow: '1', paddingX: '16px', paddingY: '15px' })}>
            <p class={css({ marginBottom: '3px', fontSize: '14px', fontWeight: 'medium', lineClamp: 1 })}>
              {attrs.title ?? '(제목 없음)'}
            </p>

            <p class={css({ fontSize: '12px', fontWeight: 'medium', color: 'text.faint', lineClamp: 2, whiteSpace: 'pre-line' })}>
              {attrs.description ?? ''}
            </p>

            <p class={css({ marginTop: 'auto', fontSize: '12px', fontWeight: 'medium', lineClamp: 1 })}>{new URL(attrs.url).origin}</p>
          </div>

          {#if attrs.thumbnailUrl}
            <img
              class={css({
                flexShrink: '0',
                borderTopRightRadius: '5px',
                borderBottomRightRadius: '5px',
                size: '118px',
                objectFit: 'cover',
              })}
              alt={attrs.title ?? '(제목 없음)'}
              src={attrs.thumbnailUrl}
            />
          {/if}
        </svelte:element>
      {/if}

      {#if editor?.current.isEditable && !window.__webview__}
        <button
          class={css({
            position: 'absolute',
            top: '20px',
            right: '20px',
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'center',
            borderRadius: '4px',
            color: 'text.bright',
            backgroundColor: '[#363839/70]',
            size: '28px',
            opacity: '0',
            transition: 'opacity',
            _hover: { backgroundColor: '[#363839/40]' },
            _groupHover: { opacity: '100' },
          })}
          onclick={() => deleteNode()}
          type="button"
        >
          <Icon icon={Trash2Icon} size={16} />
        </button>

        {#if attrs.html}
          <div
            class={flex({
              position: 'absolute',
              top: '0',
              bottom: '0',
              left: '10px',
              alignItems: 'center',
              pointerEvents: 'none',
            })}
          >
            <button
              class={css({
                borderRadius: '4px',
                backgroundColor: 'white/50',
                mixBlendMode: 'difference',
                width: '8px',
                height: '1/3',
                maxHeight: '72px',
                cursor: 'col-resize',
                opacity: '0',
                transition: 'opacity',
                pointerEvents: 'auto',
                _hover: { backgroundColor: 'white/40' },
                _groupHover: { opacity: '100' },
              })}
              aria-label="임베드 크기 조절"
              onpointerdown={(event) => {
                event.preventDefault();
                handleResizeStart(event, true);
              }}
              onpointermove={handleResize}
              onpointerup={handleResizeEnd}
              type="button"
            ></button>
          </div>

          <div
            class={flex({
              position: 'absolute',
              top: '0',
              bottom: '0',
              right: '10px',
              alignItems: 'center',
              pointerEvents: 'none',
            })}
          >
            <button
              class={css({
                borderRadius: '4px',
                backgroundColor: 'white/50',
                mixBlendMode: 'difference',
                width: '8px',
                height: '1/3',
                maxHeight: '72px',
                cursor: 'col-resize',
                opacity: '0',
                transition: 'opacity',
                pointerEvents: 'auto',
                _hover: { backgroundColor: 'white/40' },
                _groupHover: { opacity: '100' },
              })}
              aria-label="임베드 크기 조절"
              onpointerdown={(event) => {
                event.preventDefault();
                handleResizeStart(event, false);
              }}
              onpointermove={handleResize}
              onpointerup={handleResizeEnd}
              type="button"
            ></button>
          </div>
        {/if}
      {/if}
    {:else}
      <div
        class={flex({
          justifyContent: 'space-between',
          alignItems: 'center',
          borderRadius: '4px',
          backgroundColor: 'surface.muted',
        })}
        use:anchor
      >
        <div
          class={flex({
            align: 'center',
            gap: '12px',
            paddingX: '14px',
            paddingY: '12px',
            fontSize: '14px',
            color: 'text.disabled',
          })}
        >
          {#if inflight}
            <RingSpinner style={css.raw({ size: '20px' })} />
            링크 임베드 중...
          {:else}
            <Icon icon={FileUpIcon} size={20} />
            {#if editor?.current.isEditable}
              링크 임베드(Youtube, Google Drive, 일반 링크 등)
            {:else}
              링크 임베드 없음
            {/if}
          {/if}
        </div>

        {#if editor?.current.isEditable && !window.__webview__}
          <Menu>
            {#snippet button({ open })}
              <div
                class={css(
                  {
                    marginRight: '12px',
                    borderRadius: '4px',
                    padding: '2px',
                    color: 'text.disabled',
                    opacity: '0',
                    transition: 'common',
                    _hover: { backgroundColor: 'interactive.hover' },
                    _groupHover: { opacity: '100' },
                  },
                  open && { opacity: '100' },
                )}
              >
                <Icon icon={EllipsisIcon} size={20} />
              </div>
            {/snippet}

            <MenuItem onclick={() => deleteNode()} variant="danger">
              <Icon icon={Trash2Icon} size={12} />
              <span>삭제</span>
            </MenuItem>
          </Menu>
        {/if}
      </div>
    {/if}
  </div>
</NodeView>

{#if pickerOpened && !attrs.id && !inflight && editor?.current.isEditable && !window.__webview__}
  <form
    class={center({
      flexDirection: 'column',
      gap: '12px',
      borderWidth: '1px',
      borderRadius: '12px',
      padding: '12px',
      width: '380px',
      backgroundColor: 'surface.default',
      boxShadow: 'small',
      zIndex: 'editor',
    })}
    onsubmit={(e) => {
      e.preventDefault();
      handleInsert();
    }}
    use:floating
  >
    <span class={css({ fontSize: '13px', color: 'text.muted', textAlign: 'center' })}>
      Youtube, Google Drive, 일반 링크 등
      <br />
      다양한 콘텐츠를 임베드할 수 있어요
    </span>

    <TextInput
      name="url"
      style={css.raw({ width: 'full' })}
      placeholder="https://..."
      size="sm"
      bind:element={inputEl}
      bind:value={inflightUrl}
    />

    <Button style={css.raw({ width: 'full' })} size="sm" type="submit">확인</Button>
  </form>
{/if}
