#!/usr/bin/env node

// 러너 variants.json에서 지정한 라벨들의 프롬프트(러너 로컬 파일)를 읽어
// prompt_variants 시드 SQL을 산출한다. 산출물은 이 레포가 아니라 러너의 sql/ 디렉토리에 쓴다.
//
// 사용법: node scripts/seed-variants.ts <runner-path> --labels 현행,독자,코치,강점 [--out <file>]
//   첫 라벨이 계보의 루트(base)가 된다. 기본 out: <runner-path>/sql/seed-variants.sql
//   산출 SQL은 같은 시드 id를 먼저 DELETE하는 멱등 형태.
// 적용: wrangler d1 execute typie-eval --remote --file=<out-file>  (apps/eval에서)

import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { parseArgs } from 'node:util';
import type { StageKey, StagePrompt, VariantContent } from '../src/lib/domain/admin-types.ts';

type VariantManifestEntry = Record<StageKey, string> & { note?: string | null };
type VariantManifest = Record<string, VariantManifestEntry>;

const expandHome = (p: string) => (p.startsWith('~') ? path.join(os.homedir(), p.slice(1)) : p);

const escapeSqlString = (value: string) => value.replaceAll("'", "''");
const sqlString = (value: string) => `'${escapeSqlString(value)}'`;
const sqlJson = (value: unknown) => sqlString(JSON.stringify(value));
const sqlNullable = (value: string | null) => (value === null ? 'NULL' : sqlString(value));

const readStagePrompt = (runnerPath: string, relDir: string): StagePrompt => {
  const dir = path.join(runnerPath, 'prompts', relDir);

  const system = fs.readFileSync(path.join(dir, 'system.md'), 'utf8');
  const tools = JSON.parse(fs.readFileSync(path.join(dir, 'tools.json'), 'utf8')) as Record<string, unknown>;
  const config = JSON.parse(fs.readFileSync(path.join(dir, 'config.json'), 'utf8')) as { model: string; effort: string | null };

  return { system, tools, model: config.model, effort: config.effort ?? null };
};

const { positionals, values } = parseArgs({
  allowPositionals: true,
  options: { labels: { type: 'string' }, out: { type: 'string' }, prefix: { type: 'string' }, base: { type: 'string' } },
});
const runnerArg = positionals[0];

if (!runnerArg || !values.labels) {
  console.error(
    'Usage: node scripts/seed-variants.ts <runner-path> --labels 라벨1,라벨2,... [--out <file>] [--prefix seed-r2] [--base <기존 variant id>]',
  );
  process.exit(1);
}

const runnerPath = path.resolve(expandHome(runnerArg));
const outPath = values.out ? path.resolve(expandHome(values.out)) : path.join(runnerPath, 'sql', 'seed-variants.sql');

const manifestPath = path.join(runnerPath, 'variants.json');
const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8')) as VariantManifest;

const requestedLabels = values.labels
  .split(',')
  .map((label) => label.trim())
  .filter(Boolean);
const missing = requestedLabels.filter((label) => !Object.hasOwn(manifest, label));
if (missing.length > 0) {
  console.error(`labels not in ${manifestPath}: ${missing.join(', ')}`);
  process.exit(1);
}
const candidateEntries = requestedLabels.map((label) => [label, manifest[label]] as const);

const seedPrefix = values.prefix ?? 'seed-r1';
const seedId = (key: string, index: number) => `${seedPrefix}-${String(index + 1).padStart(2, '0')}-${key}`;
const rootKey = values.base ? null : requestedLabels[0];
const rootId = values.base ?? seedId(requestedLabels[0], 0);

const now = Math.floor(Date.now() / 1000);

const rows = candidateEntries.map(([key, entry], index) => {
  const content: VariantContent = {
    summarize: readStagePrompt(runnerPath, entry.summarize),
    meta: readStagePrompt(runnerPath, entry.meta),
    analyze: readStagePrompt(runnerPath, entry.analyze),
  };

  return {
    id: seedId(key, index),
    label: key,
    note: entry.note ?? null,
    baseVariantId: key === rootKey ? null : rootId,
    content,
    createdAt: now + index,
  };
});

const deleteStatement = `DELETE FROM prompt_variants WHERE id IN (${rows.map((row) => sqlString(row.id)).join(', ')});`;

const statements = rows.map(
  (row) =>
    `INSERT INTO prompt_variants (id, label, note, base_variant_id, content, status, created_at, updated_at) VALUES (${sqlString(row.id)}, ${sqlString(row.label)}, ${sqlNullable(row.note)}, ${sqlNullable(row.baseVariantId)}, ${sqlJson(row.content)}, 'draft', ${row.createdAt}, ${row.createdAt});`,
);

const header = [
  '-- generated by apps/eval/scripts/seed-variants.ts',
  `-- source: ${manifestPath}`,
  `-- generated_at: ${new Date(now * 1000).toISOString()}`,
  `-- variants: ${rows.map((row) => `${row.label}(${row.id}, base=${row.baseVariantId ?? 'none'})`).join(', ')}`,
  '-- 프롬프트 내용은 이 SQL 파일에만 존재한다 — 레포에 커밋 금지',
].join('\n');

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, `${header}\n\n${deleteStatement}\n\n${statements.join('\n\n')}\n`);

for (const row of rows) {
  console.log(`✓ ${row.label} → ${row.id} (base=${row.baseVariantId ?? 'none'})`);
}
console.log(`wrote ${rows.length} variant(s) to ${outPath}`);
